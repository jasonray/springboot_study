package example.component;

public class Range {
	private String from;
	private String to;
	private Raw raw;

	public Raw getRaw() {
		return raw;
	}

	public void setRaw(Raw data) {
		raw = data;
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String data) {
		from = data;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String data) {
		to = data;
	}
}
